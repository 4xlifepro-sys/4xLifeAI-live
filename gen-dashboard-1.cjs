const fs = require('fs');
const lines = [];
lines.push("import { useEffect, useState } from 'react';");
lines.push("import { useNavigate } from 'react-router-dom';");
lines.push("import { supabase } from '../lib/supabase';");
lines.push("");
lines.push("const APPROVED = ['EURUSD','USDJPY','USDCAD','NZDUSD','EURJPY','GBPJPY','XAUUSD','XAGUSD','BTCUSD','ETHUSD'];");
lines.push("");
lines.push("function fp(n: number, pair: string) {");
lines.push("  if (!n) return '--';");
lines.push("  if (pair.includes('JPY')) return n.toFixed(3);");
lines.push("  if (n >= 10000) return n.toLocaleString('en-US',{minimumFractionDigits:2,maximumFractionDigits:2});");
lines.push("  if (n >= 100) return n.toFixed(2);");
lines.push("  if (n >= 1) return n.toFixed(4);");
lines.push("  return n.toFixed(5);");
lines.push("}");
lines.push("");
lines.push("function getSession() {");
lines.push("  const h = new Date().getUTCHours();");
lines.push("  if (h >= 13 && h < 17) return { name: 'NEW YORK', color: '#10b981', bg: 'bg-emerald-500/10 border-emerald-500/20' };");
lines.push("  if (h >= 7 && h < 13) return { name: 'LONDON', color: '#3b82f6', bg: 'bg-blue-500/10 border-blue-500/20' };");
lines.push("  if (h >= 0 && h < 7) return { name: 'TOKYO', color: '#f59e0b', bg: 'bg-amber-500/10 border-amber-500/20' };");
lines.push("  return { name: 'SYDNEY', color: '#8b5cf6', bg: 'bg-purple-500/10 border-purple-500/20' };");
lines.push("}");
lines.push("");
lines.push("export default function Dashboard() {");
lines.push("  const nav = useNavigate();");
lines.push("  const [state, setState] = useState<any>({ stats:null, marketStates:[], activeOpportunities:[], prices:{} });");
lines.push("");
lines.push("  useEffect(() => {");
lines.push("    const es = new EventSource('/api/stream');");
lines.push("    es.onmessage = (e) => { try { setState(p=>({...p,...JSON.parse(e.data)})); } catch {} };");
lines.push("    const fetchP = async () => {");
lines.push("      try { const r = await fetch('/api/prices'); if (!r.ok) return; const d = await r.json(); const m:Record<string,number>={}; (d.prices||[]).forEach((p:any)=>{ if(p.pair&&typeof p.price==='number') m[p.pair]=p.price; }); setState(p=>({...p,prices:m})); } catch {}");
lines.push("    };");
lines.push("    const fetchS = async () => {");
lines.push("      if (!supabase) return;");
lines.push("      const { data } = await supabase.from('signals').select('*').order('created_at',{ascending:false}).limit(100);");
lines.push("      if (data) setState(p=>({...p,activeOpportunities:data}));");
lines.push("    };");
lines.push("    fetchP(); fetchS();");
lines.push("    const pi = setInterval(fetchP, 5000);");
lines.push("    const si = setInterval(fetchS, 15000);");
lines.push("    let ch: any;");
lines.push("    if (supabase) { ch = supabase.channel('d2').on('postgres_changes',{event:'*',schema:'public',table:'signals'},(pl:any)=>{ if(pl.eventType==='INSERT') setState(p=>({...p,activeOpportunities:[pl.new,...(p.activeOpportunities||[])].slice(0,100)})); else if(pl.eventType==='UPDATE') setState(p=>({...p,activeOpportunities:(p.activeOpportunities||[]).map((s:any)=>s.id===pl.new.id?pl.new:s)})); }).subscribe(); }");
lines.push("    return () => { es.close(); if(ch) supabase!.removeChannel(ch); clearInterval(pi); clearInterval(si); };");
lines.push("  }, []);");
lines.push("");
lines.push("  const prices = state.prices || {};");
lines.push("  const marketStates = (state.marketStates||[]).filter((s:any)=>APPROVED.includes(s.pair));");
lines.push("  const allSig = state.activeOpportunities || [];");
lines.push("  const active = allSig.filter((s:any)=>APPROVED.includes(s.pair)&&['ACTIVE','TP1 HIT','TP2 HIT'].includes(s.status));");
lines.push("  const closed = allSig.filter((s:any)=>APPROVED.includes(s.pair)&&['CLOSED','TP3 HIT','SL HIT','EXPIRED'].includes(s.status));");
lines.push("  const sess = getSession();");
lines.push("  const total = APPROVED.length;");
lines.push("  const bullish = marketStates.filter((m:any)=>m.direction==='LONG').length;");
lines.push("  const bearish = marketStates.filter((m:any)=>m.direction==='SHORT').length;");
lines.push("  const neutral = total - bullish - bearish;");

fs.writeFileSync('C:\\Users\\PC\\Documents\\GitHub\\4xLifeAI-live\\src\\pages\\Dashboard.tsx', lines.join('\n') + '\n', 'utf8');
console.log('Part 1 done');
